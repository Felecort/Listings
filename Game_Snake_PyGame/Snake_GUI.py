	# Проверка на победу / поражение 
	def Check_Victory_Lose(self):
		if len(self.indicator) == 0:
			self.game = "LOSE"
		elif len(self.indicator) == 37: #кол-во единиц  еды - 1,  которое необходимо достичь для победы
			self.game = "Victory" 
